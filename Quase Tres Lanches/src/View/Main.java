    private void jButton15ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton15ActionPerformed
        String nomeP = jComboBox7.getSelectedItem().toString();
        String inf1 = jnome1.getSelectedItem().toString();
        String inf2 = jnome2.getSelectedItem().toString();
        String inf3 = jnome3.getSelectedItem().toString();
        Integer quantidade = (int) jSpinner1.getValue();
        double precoTotal = Double.parseDouble(totaltxt.getText());
        
        // Carregar vendas do arquivo (caso j√° existam)
        Venda vendas = Persistencia.carregarVendas();

        // Criando o produto correspondente
        if (nomeP.equalsIgnoreCase("Pizza")) {
            double precoPizza = obterPrecoOuSolicitar(jnome1, jnome2, jnome3, pizzas, "getRecheio", "getBorda", "getMolho", "getPreco");
            Pizza pizza = new Pizza(inf1, inf2, inf3, precoPizza); // Adaptar o construtor √† classe Pizza
            vendas.registrarVenda("Pizza", pizza, quantidade, precoTotal); // Pre√ßo unit√°rio = total / quantidade
        } else if (nomeP.equalsIgnoreCase("Salgado")) {
            double precoSalgado = obterPrecoOuSolicitar(jnome1, jnome2, jnome3, salgadinhos, "getTipo", "getMassa", "getRecheio", "getPreco");
            Salgado salgado = new Salgado(inf1, inf2, inf3, precoSalgado); // Adaptar o construtor √† classe Salgado
            vendas.registrarVenda("salgado", salgado, quantidade, precoTotal); // Pre√ßo unit√°rio = total / quantidade
        }

        // Salvar a venda no arquivo
        Persistencia.salvarVendas(vendas);

        // Mensagem de confirma√ß√£o
        JOptionPane.showMessageDialog(this, "Venda registrada com sucesso!");

    }//GEN-LAST:event_jButton15ActionPerformed

